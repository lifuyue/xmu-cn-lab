#ifndef RS232C_HPP
#define RS232C_HPP

// 这个头文件定义了 LAB2 的公共接口。
// 题目要求实现两个核心函数：
// 1. 把字符串编码成 RS232C 电压序列
// 2. 把 RS232C 电压序列解码回字符串
//
// 本实验采用如下固定约定：
// - 标准 RS232C 极性：逻辑 1 为负电压，逻辑 0 为正电压
// - 默认电压幅值：逻辑 1 使用 -12V，逻辑 0 使用 +12V
// - 每个字符都编码成 10 个电压值：
//   空闲位 + 起始位 + 7 位数据 + 终止位
// - 位序采用“小端位序”，即数据位按 LSB -> MSB 输出
// - 字节顺序采用输入字符串的自然顺序

// 把长度为 size 的消息编码为电压序列。
//
// 参数：
// - volts：输出电压数组
// - volts_size：输出数组容量
// - msg：输入消息
// - size：输入消息的字符数
//
// 返回值：
// - 成功：返回实际写入的电压数量
// - 失败：返回 -1
//
// 失败条件包括：
// - 参数非法
// - 输出缓冲区空间不足
// - 输入中含有非 7-bit ASCII 字符
int rs232c_encode(double* volts, int volts_size, const char* msg, int size);

// 把电压序列解码为消息。
//
// 参数：
// - msg：输出字符数组
// - size：输出数组可容纳的字符数
// - volts：输入电压数组
// - volts_size：输入电压数量
//
// 返回值：
// - 成功：返回实际解码得到的字符数量
// - 失败：返回 -1
//
// 失败条件包括：
// - 参数非法
// - 输入长度不是 10 的整数倍
// - 输入缓冲区空间不足
// - 空闲位 / 起始位 / 终止位格式错误
int rs232c_decode(char* msg, int size, const double* volts, int volts_size);

#endif  // RS232C_HPP
